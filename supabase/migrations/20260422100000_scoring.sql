-- Scoring system: lead_source, score_rules, score_events, score_targets, triggers, views.

-- 1. Add lead_source to existing tables
alter table public.companies  add column if not exists lead_source text not null default 'unknown';
alter table public.contacts   add column if not exists lead_source text not null default 'unknown';
alter table public.deals      add column if not exists lead_source text not null default 'unknown';

-- 2. Scoring tables
create table if not exists public.score_rules (
    id bigint generated by default as identity primary key,
    code text not null unique,
    domain text not null check (domain in ('sales', 'delivery')),
    label text not null,
    weight_class text not null check (weight_class in ('acquisition', 'execution', 'revenue', 'none')),
    base_points numeric not null,
    enabled boolean not null default true,
    config jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now()
);

create table if not exists public.score_events (
    id bigint generated by default as identity primary key,
    sales_id bigint not null references public.sales(id) on delete cascade,
    domain text not null check (domain in ('sales', 'delivery')),
    rule_code text not null,
    entity_type text not null,
    entity_id bigint,
    raw_points numeric not null,
    weight numeric not null default 1,
    points numeric not null,
    occurred_at timestamptz not null default now(),
    period_day date not null default current_date,
    idempotency_key text not null unique,
    metadata jsonb not null default '{}'::jsonb
);

create index if not exists score_events_sales_id_idx  on public.score_events using btree (sales_id);
create index if not exists score_events_period_day_idx on public.score_events using btree (period_day);
create index if not exists score_events_domain_idx     on public.score_events using btree (domain);

create table if not exists public.score_targets (
    id bigint generated by default as identity primary key,
    domain text not null check (domain in ('sales', 'delivery')),
    period text not null check (period in ('week', 'month', 'quarter')),
    target_score numeric not null,
    unique (domain, period)
);

