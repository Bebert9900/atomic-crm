-- Helper function to encrypt/decrypt passwords using a server-side key
create or replace function public.encrypt_email_password(plain_password text)
returns text
language plpgsql security definer
set search_path to ''
as $$
declare
    encryption_key text;
begin
    encryption_key := current_setting('app.settings.email_encryption_key', true);
    if encryption_key is null or encryption_key = '' then
        raise exception 'Email encryption key not configured';
    end if;
    return encode(
        extensions.pgp_sym_encrypt(plain_password, encryption_key),
        'base64'
    );
end;
$$;

create or replace function public.decrypt_email_password(encrypted_password text)
returns text
language plpgsql security definer
set search_path to ''
as $$
declare
    encryption_key text;
begin
    encryption_key := current_setting('app.settings.email_encryption_key', true);
    if encryption_key is null or encryption_key = '' then
        raise exception 'Email encryption key not configured';
    end if;
    return extensions.pgp_sym_decrypt(
        decode(encrypted_password, 'base64'),
        encryption_key
    );
end;
$$;

-- Encryption function is callable by authenticated users (so admins can save
-- accounts from the UI). Writes to email_accounts are still gated by the
-- is_admin() RLS policy. Decryption stays service_role-only (sync worker / edge).
-- We explicitly revoke from anon/authenticated because the schema's default
-- privileges auto-grant all functions to those roles.
revoke all on function public.encrypt_email_password(text) from public;
revoke all on function public.encrypt_email_password(text) from anon;
grant execute on function public.encrypt_email_password(text) to authenticated;
grant execute on function public.encrypt_email_password(text) to service_role;

revoke all on function public.decrypt_email_password(text) from public;
revoke all on function public.decrypt_email_password(text) from anon;
revoke all on function public.decrypt_email_password(text) from authenticated;
grant execute on function public.decrypt_email_password(text) to service_role;

-- Unread emails summary view for dashboard
create or replace view public.unread_emails_summary with (security_invoker = on) as
select
    em.id,
    em.from_email,
    em.from_name,
    em.subject,
    em.date,
    em.contact_id,
    em.sales_id,
    em.email_account_id,
    ea.email as account_email,
    c.first_name as contact_first_name,
    c.last_name as contact_last_name
from public.email_messages em
join public.email_accounts ea on ea.id = em.email_account_id
left join public.contacts c on c.id = em.contact_id
where em.is_read = false
  and em.folder = 'INBOX'
order by em.date desc;

grant all on table public.unread_emails_summary to anon;
grant all on table public.unread_emails_summary to authenticated;
grant all on table public.unread_emails_summary to service_role;
