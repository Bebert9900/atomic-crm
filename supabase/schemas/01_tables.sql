create table public.email_accounts (
    id bigint generated by default as identity primary key,
    email extensions.citext not null unique,
    imap_host text not null,
    imap_port integer not null default 993,
    smtp_host text,
    smtp_port integer not null default 465,
    encrypted_password text not null,
    sales_id bigint,
    is_active boolean not null default true,
    skip_tls_verify boolean not null default false,
    created_at timestamp with time zone not null default now()
);

alter table public.email_accounts
    add constraint email_accounts_sales_id_fkey foreign key (sales_id) references public.sales(id);

create table public.email_messages (
    id bigint generated by default as identity primary key,
    message_id text not null,
    email_account_id bigint not null,
    folder text not null,
    from_email text not null,
    from_name text,
    to_emails jsonb,
    cc_emails jsonb,
    subject text,
    text_body text,
    html_body text,
    date timestamp with time zone not null,
    is_read boolean not null default false,
    contact_id bigint,
    sales_id bigint,
    uid integer,
    created_at timestamp with time zone not null default now(),
    unique(email_account_id, folder, message_id)
);

alter table public.email_messages
    add constraint email_messages_email_account_id_fkey foreign key (email_account_id) references public.email_accounts(id) on delete cascade;

alter table public.email_messages
    add constraint email_messages_contact_id_fkey foreign key (contact_id) references public.contacts(id) on update cascade on delete set null;

alter table public.email_messages
    add constraint email_messages_sales_id_fkey foreign key (sales_id) references public.sales(id);

create index email_messages_email_account_id_idx on public.email_messages using btree (email_account_id);
create index email_messages_contact_id_idx on public.email_messages using btree (contact_id);
create index email_messages_date_idx on public.email_messages using btree (date desc);
create index email_messages_is_read_idx on public.email_messages using btree (is_read) where is_read = false;
create index email_messages_message_id_idx on public.email_messages using btree (message_id);

create table public.email_sync_state (
    id bigint generated by default as identity primary key,
    email_account_id bigint not null,
    folder text not null,
    last_uid integer not null default 0,
    last_sync timestamp with time zone,
    unique(email_account_id, folder)
);

alter table public.email_sync_state
    add constraint email_sync_state_email_account_id_fkey foreign key (email_account_id) references public.email_accounts(id) on delete cascade;

